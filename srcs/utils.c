#include "main.h"

int		screen_to_board(int x, int y)
{
	int		result;

	result = (x / 64) + ((y / 64) * 3);
	return (result);
}

void	get_loc(int i, int *x, int *y)
{
	*x = i % 3;
	*y = i / 3;

	*y *= 64;
	*x *= 64;
}
